import UIKit

extension UIColor {
    
  class var nearWhite: UIColor {
    return UIColor(white: 249.0 / 255.0, alpha: 1.0)
  }

  class var greenPersonal: UIColor {
    return UIColor(red: 138.0 / 255.0, green: 189.0 / 255.0, blue: 55.0 / 255.0, alpha: 1.0)
  }

  class var grayPersonal: UIColor {
    return UIColor(white: 232.0 / 255.0, alpha: 1.0)
  }
    
    class var blackTraslucid: UIColor {
        return UIColor(white: 0 / 255.0, alpha: 0.4)
    }

  

}
